package dev.thyexp;

import org.thymeleaf.TemplateEngine;
import org.thymeleaf.context.Context;
import org.thymeleaf.templatemode.TemplateMode;
import org.thymeleaf.templateresolver.ClassLoaderTemplateResolver;

import java.io.StringWriter;
import java.util.Arrays;
import java.util.List;
import java.util.Locale;

public class Main {

    public static void main(String[] args) {
        if (args.length < 1) {
            System.err.println("Usage: Main <op>  (prepare | html-render | text-render | xml-render | fragment-render)");
            System.exit(1);
        }
        switch (args[0]) {
            case "prepare":         System.out.println("OK"); break;
            case "html-render":     render(TemplateMode.HTML, "templates/html/",     ".html", "main");       break;
            case "text-render":     render(TemplateMode.TEXT, "templates/text/",     ".txt",  "report");     break;
            case "xml-render":      render(TemplateMode.XML,  "templates/xml/",      ".xml",  "catalog");    break;
            case "fragment-render": render(TemplateMode.HTML, "templates/fragment/", ".html", "page");       break;
            default:
                System.err.println("Unknown op: " + args[0]);
                System.exit(1);
        }
    }

    static void render(TemplateMode mode, String prefix, String suffix, String template) {
        ClassLoaderTemplateResolver resolver = new ClassLoaderTemplateResolver();
        resolver.setTemplateMode(mode);
        resolver.setPrefix(prefix);
        resolver.setSuffix(suffix);
        resolver.setCharacterEncoding("UTF-8");

        TemplateEngine engine = new TemplateEngine();
        engine.setTemplateResolver(resolver);

        Context ctx = new Context(Locale.ENGLISH);
        ctx.setVariable("title",       "Thymeleaf AOT Benchmark");
        ctx.setVariable("heading",     "Product Catalog");
        ctx.setVariable("description", "A comprehensive listing of available products.");
        ctx.setVariable("showFooter",  Boolean.TRUE);
        ctx.setVariable("footerText",  "Generated by Thymeleaf 3.1.5.RELEASE");
        ctx.setVariable("items",       sampleItems());

        StringWriter out = new StringWriter();
        engine.process(template, ctx, out);
        System.out.println(out.toString().length());
    }

    static List<Item> sampleItems() {
        return Arrays.asList(
            new Item(1,  "Widget Alpha",   "Electronics"),
            new Item(2,  "Gadget Beta",    "Electronics"),
            new Item(3,  "Tool Gamma",     "Hardware"),
            new Item(4,  "Supply Delta",   "Office"),
            new Item(5,  "Device Epsilon", "Electronics"),
            new Item(6,  "Part Zeta",      "Hardware"),
            new Item(7,  "Item Eta",       "Misc"),
            new Item(8,  "Object Theta",   "Misc"),
            new Item(9,  "Thing Iota",     "Office"),
            new Item(10, "Unit Kappa",     "Electronics")
        );
    }

    public static class Item {
        private final int id;
        private final String name;
        private final String category;

        Item(int id, String name, String category) {
            this.id = id;
            this.name = name;
            this.category = category;
        }

        public int getId()          { return id; }
        public String getName()     { return name; }
        public String getCategory() { return category; }
    }
}
